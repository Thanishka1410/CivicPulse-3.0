// Language switching functionality
document.addEventListener('DOMContentLoaded', function() {
    // Set default language
    let currentLanguage = localStorage.getItem('selectedLanguage') || 'en';
    
    // Update language on page load
    updateLanguage(currentLanguage);
    
    // Auth page language selector
    const authLanguageBtn = document.getElementById('authLanguageBtn');
    const authLanguageDropdown = document.getElementById('authLanguageDropdown');
    
    if (authLanguageBtn) {
        authLanguageBtn.addEventListener('click', function() {
            authLanguageDropdown.classList.toggle('show');
        });
    }
    
    // Header language selector
    const headerLanguageBtn = document.getElementById('headerLanguageBtn');
    const headerLanguageDropdown = document.getElementById('headerLanguageDropdown');
    
    if (headerLanguageBtn) {
        headerLanguageBtn.addEventListener('click', function() {
            headerLanguageDropdown.classList.toggle('show');
        });
    }
    
    // Close dropdowns when clicking outside
    document.addEventListener('click', function(event) {
        if (!event.target.closest('.language-selector')) {
            if (authLanguageDropdown) authLanguageDropdown.classList.remove('show');
            if (headerLanguageDropdown) headerLanguageDropdown.classList.remove('show');
        }
    });
    
    // Language selection
    document.querySelectorAll('.language-option').forEach(option => {
        option.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            currentLanguage = lang;
            localStorage.setItem('selectedLanguage', lang);
            updateLanguage(lang);
            
            // Close dropdowns
            if (authLanguageDropdown) authLanguageDropdown.classList.remove('show');
            if (headerLanguageDropdown) headerLanguageDropdown.classList.remove('show');
        });
    });
    
    // Function to update language
    function updateLanguage(lang) {
        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
        
        // Update all elements with data-i18n-placeholder attribute
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            if (translations[lang] && translations[lang][key]) {
                element.placeholder = translations[lang][key];
            }
        });
        
        // Update active language in dropdowns
        document.querySelectorAll('.language-option').forEach(option => {
            option.classList.remove('active');
            if (option.getAttribute('data-lang') === lang) {
                option.classList.add('active');
            }
        });
        
        // Set active language for initial load
        document.querySelector(`.language-option[data-lang="${lang}"]`)?.classList.add('active');
    }
});

