// Get user location
function getUserLocation() {
    if (navigator.geolocation) {
        navigator.geolocation.getCurrentPosition(
            position => {
                const lat = position.coords.latitude;
                const lng = position.coords.longitude;
                userLocation = [lat, lng];
                
                // Update location display
                locationText.textContent = 'Location detected successfully';
                locationLoading.style.display = 'none';
                locationDisplay.innerHTML = `<i class="fas fa-map-marker-alt"></i> Current Location Detected`;
                locationInput.value = `${lat},${lng}`;
            },
            error => {
                locationText.textContent = 'Location access denied';
                locationLoading.style.display = 'none';
                locationDisplay.innerHTML = `<i class="fas fa-map-marker-alt"></i> Location access denied`;
                locationInput.value = '';
            }
        );
    } else {
        locationText.textContent = 'Geolocation not supported';
        locationLoading.style.display = 'none';
        locationDisplay.innerHTML = `<i class="fas fa-map-marker-alt"></i> Geolocation not supported`;
        locationInput.value = '';
    }
}

// AI Problem Detection using Teachable Machine model
async function detectProblemFromImage(imageFile) {
    if (!isModelLoaded) {
        // Fallback to mock detection if model isn't loaded
        return mockDetection(imageFile);
    }
    
    try {
        // Create an image element from the file
        const img = new Image();
        const imgPromise = new Promise((resolve) => {
            img.onload = resolve;
        });
        img.src = URL.createObjectURL(imageFile);
        await imgPromise;
        
        // Run prediction
        const predictions = await model.predict(img);
        
        // Find the prediction with the highest probability
        const bestPrediction = predictions.reduce((prev, current) => 
            (prev.probability > current.probability) ? prev : current
        );
        
        // Log the prediction for debugging
        console.log("AI Prediction:", bestPrediction.className, "Confidence:", bestPrediction.probability);
        
        // Map the AI label to our category - updated with exact labels from your model
        const labelToCategory = {
            "Garbage": "garbage",
            "Potholes": "pothole",
            "Electricity Issues": "streetlight",
            "Water Related Problems": "water"
        };
        
        // Get the detected category, fallback to "other" if not found
        const detectedCategory = labelToCategory[bestPrediction.className] || "other";
        
        // Generate description based on detected category
        let detectedDescription = '';
        switch (bestPrediction.className) {
            case "Garbage":
                detectedDescription = "Garbage is not being collected and needs immediate cleaning.";
                break;
            case "Potholes":
                detectedDescription = "Potholes detected on the road which may cause accidents.";
                break;
            case "Electricity Issues":
                detectedDescription = "Electricity issues detected such as streetlight not working or power outage.";
                break;
            case "Water Related Problems":
                detectedDescription = "Water related problems detected such as leakage or no water supply.";
                break;
            default:
                detectedDescription = "Issue detected. Please report to authorities.";
        }
        
        console.log("Mapped to category:", detectedCategory);
        
        return {
            originalLabel: bestPrediction.className,
            category: detectedCategory,
            description: detectedDescription,
            confidence: bestPrediction.probability
        };
    } catch (error) {
        console.error("Error during AI detection:", error);
        // Fallback to mock detection
        return mockDetection(imageFile);
    }
}

