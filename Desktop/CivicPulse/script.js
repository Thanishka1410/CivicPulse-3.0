// Toggle voice input
function toggleVoiceInput() {
    const isRecording = voiceInputBtn.classList.contains('recording');
    
    if (isRecording) {
        voiceInputBtn.classList.remove('recording');
        // Stop voice recognition
        if (window.recognition) {
            window.recognition.stop();
        }
    } else {
        voiceInputBtn.classList.add('recording');
        // Start voice recognition
        startVoiceRecognition();
    }
}

// Start voice recognition
function startVoiceRecognition() {
    if (!('webkitSpeechRecognition' in window) && !('SpeechRecognition' in window)) {
        alert('Speech recognition not supported in your browser.');
        voiceInputBtn.classList.remove('recording');
        return;
    }
    
    const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    const recognition = new SpeechRecognition();
    
    recognition.continuous = false;
    recognition.interimResults = false;
    recognition.lang = 'en-US';
    
    recognition.onresult = function(event) {
        const transcript = event.results[0][0].transcript;
        descriptionInput.value += transcript;
        voiceInputBtn.classList.remove('recording');
    };
    
    recognition.onerror = function(event) {
        console.error('Speech recognition error', event.error);
        voiceInputBtn.classList.remove('recording');
    };
    
    recognition.onend = function() {
        voiceInputBtn.classList.remove('recording');
    };
    
    recognition.start();
    window.recognition = recognition;
}

// Handle complaint submission
function handleComplaintSubmit(e) {
    e.preventDefault();
    
    const category = document.getElementById('categorySelect').value;
    const description = document.getElementById('descriptionInput').value;
    const location = document.getElementById('locationInput').value;
    
    // Generate new complaint ID
    const newId = 'CC-' + new Date().getFullYear() + '-' + String(complaints.length + 1).padStart(3, '0');
    
    // Create new complaint object
    const newComplaint = {
        id: newId,
        date: new Date().toISOString().split('T')[0],
        category: category,
        categoryName: getCategoryName(category),
        description: description,
        location: location,
        status: 'submitted',
        assignedTo: getDepartmentForCategory(category),
        coordinates: userLocation || [23.4108, 85.2799], // Default to Jharkhand center
        images: [],
        timeline: [
            { status: 'submitted', date: new Date().toISOString().split('T')[0], active: true, completed: false },
            { status: 'acknowledged', date: null, active: false, completed: false },
            { status: 'in-progress', date: null, active: false, completed: false },
            { status: 'resolved', date: null, active: false, completed: false }
        ],
        userId: currentUser.username
    };
    
    // Add images if any
    const previewItems = previewContainer.querySelectorAll('.preview-item');
    previewItems.forEach(item => {
        const img = item.querySelector('img');
        const video = item.querySelector('video');
        if (img) {
            newComplaint.images.push(img.src);
        } else if (video) {
            newComplaint.images.push(video.src);
        }
    });
    
    // Add complaint to array
    complaints.push(newComplaint);
    
    // Save to localStorage
    saveDataToStorage();
    
    // Add points to user if citizen
    if (currentUser.role === 'citizen') {
        currentUser.points += 100;
        
        // Update user in users array
        const userIndex = users.findIndex(u => u.username === currentUser.username);
        if (userIndex !== -1) {
            users[userIndex].points = currentUser.points;
        }
        
        // Save to localStorage
        saveDataToStorage();
        
        // Update points display
        updateUserPoints();
    }
    
    // Show success message
    alert(`Complaint submitted successfully!\n\nCategory: ${category}\nDescription: ${description}\nLocation: ${location}\n\nYou earned 100 points!`);
    
    // Reset form
    complaintForm.reset();
    previewContainer.innerHTML = '';
    aiDetection.classList.add('hidden');
    
    // Switch to track tab
    showTab('track');
    
    // Reload complaints list
    loadComplaints();
}

