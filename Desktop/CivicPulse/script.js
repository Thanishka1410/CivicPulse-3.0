// Filter complaints in admin view
function filterComplaints() {
    const statusValue = statusFilter.value;
    const categoryValue = categoryFilter.value;
    const dateValue = dateFilter.value;
    
    let filteredComplaints = [...complaints];
    
    // Apply status filter
    if (statusValue) {
        filteredComplaints = filteredComplaints.filter(c => c.status === statusValue);
    }
    
    // Apply category filter
    if (categoryValue) {
        filteredComplaints = filteredComplaints.filter(c => c.category === categoryValue);
    }
    
    // Apply date filter
    if (dateValue) {
        const today = new Date();
        const filterDate = new Date();
        
        switch (dateValue) {
            case 'today':
                filterDate.setHours(0, 0, 0, 0);
                break;
            case 'week':
                filterDate.setDate(today.getDate() - 7);
                break;
            case 'month':
                filterDate.setMonth(today.getMonth() - 1);
                break;
            case 'quarter':
                filterDate.setMonth(today.getMonth() - 3);
                break;
        }
        
        filteredComplaints = filteredComplaints.filter(c => {
            const complaintDate = new Date(c.date);
            return complaintDate >= filterDate;
        });
    }
    
    // Update table with filtered results
    complaintsTableBody.innerHTML = filteredComplaints.map(complaint => `
        <tr>
            <td>${complaint.id}</td>
            <td>${complaint.date}</td>
            <td>${complaint.categoryName}</td>
            <td>${complaint.location}</td>
            <td><span class="status-badge status-${complaint.status}">${formatStatus(complaint.status)}</span></td>
            <td>${complaint.assignedTo}</td>
            <td>
                <div class="action-buttons">
                    <button class="action-btn view-btn" data-id="${complaint.id}">
                        <i class="fas fa-eye"></i>
                    </button>
                    <button class="action-btn edit-btn" data-id="${complaint.id}">
                        <i class="fas fa-edit"></i>
                    </button>
                </div>
            </td>
        </tr>
    `).join('');
    
    // Re-attach event listeners
    document.querySelectorAll('.view-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const complaintId = this.getAttribute('data-id');
            showComplaintDetail(complaintId);
        });
    });
    
    document.querySelectorAll('.edit-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const complaintId = this.getAttribute('data-id');
            alert(`Edit complaint ${complaintId}`);
        });
    });
}

