// Update user profile data
function updateUserProfile() {
    if (!currentUser) return;
    
    // Update user stats
    const userComplaints = complaints.filter(c => c.userId === currentUser.username);
    const resolvedComplaints = userComplaints.filter(c => c.status === 'resolved');
    
    profileComplaintsCount.textContent = userComplaints.length;
    profileResolvedCount.textContent = resolvedComplaints.length;
    
    // Calculate badges and rank
    const badgesCount = Math.floor(currentUser.points / 500);
    profileBadgesCount.textContent = badgesCount;
    
    let rank = 'Beginner';
    if (currentUser.points >= 2000) rank = 'Expert';
    else if (currentUser.points >= 1000) rank = 'Advanced';
    else if (currentUser.points >= 500) rank = 'Intermediate';
    
    profileRank.textContent = rank;
    
    // Update recent activity
    if (userComplaints.length > 0) {
        const recentComplaints = userComplaints.slice(-3).reverse();
        recentActivity.innerHTML = recentComplaints.map(complaint => `
            <div style="padding: 10px 0; border-bottom: 1px solid var(--light-gray);">
                <div style="font-weight: 500;">${complaint.categoryName}</div>
                <div style="font-size: 0.9rem; color: var(--gray-color);">${complaint.description.substring(0, 50)}${complaint.description.length > 50 ? '...' : ''}</div>
                <div style="font-size: 0.8rem; color: var(--gray-color);">${complaint.date} - ${formatStatus(complaint.status)}</div>
            </div>
        `).join('');
    } else {
        recentActivity.innerHTML = '<p style="color: var(--gray-color); text-align: center; padding: 20px 0;">No recent activity</p>';
    }
}

