// Load data from localStorage
function loadDataFromStorage() {
    // Load complaints from localStorage
    const storedComplaints = localStorage.getItem('civicConnectComplaints');
    if (storedComplaints) {
        complaints = JSON.parse(storedComplaints);
    }
    
    // Load users from localStorage
    const storedUsers = localStorage.getItem('civicConnectUsers');
    if (storedUsers) {
        const parsedUsers = JSON.parse(storedUsers);
        // Merge with default users, preserving any new users
        parsedUsers.forEach(user => {
            const existingUserIndex = users.findIndex(u => u.username === user.username);
            if (existingUserIndex !== -1) {
                users[existingUserIndex] = user;
            } else {
                users.push(user);
            }
        });
    }
}

// Save data to localStorage
function saveDataToStorage() {
    localStorage.setItem('civicConnectComplaints', JSON.stringify(complaints));
    localStorage.setItem('civicConnectUsers', JSON.stringify(users));
}

// Authentication functions
function showAuthForm(formType) {
    if (formType === 'login') {
        loginTab.classList.add('active');
        signupTab.classList.remove('active');
        loginForm.classList.remove('hidden');
        signupForm.classList.add('hidden');
    } else {
        loginTab.classList.remove('active');
        signupTab.classList.add('active');
        loginForm.classList.add('hidden');
        signupForm.classList.remove('hidden');
    }
    
    // Hide alerts
    loginAlert.classList.add('hidden');
    signupAlert.classList.add('hidden');
}

function handleLogin() {
    const username = document.getElementById('loginUsername').value.trim();
    const password = document.getElementById('loginPassword').value;
    
    if (!username || !password) {
        showAlert('loginAlert', 'Please enter both username and password', 'danger');
        return;
    }
    
    // Find user in sample data
    const user = users.find(u => u.username === username && u.password === password);
    
    if (user) {
        // Store user in localStorage
        localStorage.setItem('civicConnectUser', JSON.stringify(user));
        currentUser = user;
        
        // Show main app
        showMainApp();
    } else {
        showAlert('loginAlert', 'Invalid username or password', 'danger');
    }
}

function handleSignup() {
    const name = document.getElementById('signupName').value.trim();
    const username = document.getElementById('signupUsername').value.trim();
    const email = document.getElementById('signupEmail').value.trim();
    const password = document.getElementById('signupPassword').value;
    const confirmPassword = document.getElementById('signupConfirmPassword').value;
    
    // Validation
    if (!selectedRole) {
        showAlert('signupAlert', 'Please select your role', 'danger');
        return;
    }
    
    if (!name || !username || !email || !password || !confirmPassword) {
        showAlert('signupAlert', 'Please fill in all fields', 'danger');
        return;
    }
    
    if (password !== confirmPassword) {
        showAlert('signupAlert', 'Passwords do not match', 'danger');
        return;
    }
    
    // Check if username already exists
    if (users.some(u => u.username === username)) {
        showAlert('signupAlert', 'Username already exists', 'danger');
        return;
    }
    
    // Validate official code
    if (selectedRole === 'official') {
        const officialCode = officialCodeInput.value.trim();
        if (officialCode !== 'ResolveX') {
            showAlert('signupAlert', 'Invalid official code', 'danger');
            return;
        }
    }
    
    // Create new user
    const newUser = {
        username,
        password,
        role: selectedRole,
        name,
        points: selectedRole === 'citizen' ? 0 : 0  // Citizens start with 0 points
    };
    
    // Add to users array (in real app, this would be sent to server)
    users.push(newUser);
    saveDataToStorage();
    
    // Show success message
    showAlert('signupAlert', 'Account created successfully! Please login.', 'success');
    
    // Switch to login form after a delay
    setTimeout(() => {
        showAuthForm('login');
        // Clear form
        document.getElementById('signupName').value = '';
        document.getElementById('signupUsername').value = '';
        document.getElementById('signupEmail').value = '';
        document.getElementById('signupPassword').value = '';
        document.getElementById('signupConfirmPassword').value = '';
        roleOptions.forEach(opt => opt.classList.remove('selected'));
        selectedRole = '';
    }, 1500);
}

function showAlert(alertId, message, type) {
    const alert = document.getElementById(alertId);
    alert.textContent = message;
    alert.className = `alert alert-${type}`;
    alert.classList.remove('hidden');
}

