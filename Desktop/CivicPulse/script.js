// Load the AI model
// Removed duplicate definition of loadModel
async function loadModel() {
    try {
        console.log("Attempting to load AI model...");
        console.log("Model URL:", MODEL_URL + "model.json");
        model = await tf.loadLayersModel(MODEL_URL + "model.json");
        isModelLoaded = true;
        console.log("AI Model Loaded Successfully");
    } catch (error) {
        console.error("Error loading AI model:", error);
        console.error("Error details:", {
            message: error.message,
            stack: error.stack,
            name: error.name
        });
        isModelLoaded = false;
    }
}

// Function to handle image upload and prediction
async function handleImageUpload(event) {
    if (!isModelLoaded) {
        alert("AI model is not loaded yet. Please try again later.");
        return;
    }

    const file = event.target.files[0];
    if (!file) {
        alert("No file selected. Please upload an image.");
        return;
    }

    const reader = new FileReader();
    reader.onload = async function(e) {
        const img = new Image();
        img.src = e.target.result;
        img.onload = async function() {
            // Preprocess the image
            const tensor = tf.browser.fromPixels(img)
                .resizeNearestNeighbor([224, 224]) // Resize to MobileNetV2 input size
                .toFloat()
                .expandDims();

            // Make predictions
            const predictions = await model.predict(tensor).data();
            const topPredictionIndex = predictions.indexOf(Math.max(...predictions));

            // Map the prediction index to class labels
            const classLabels = ["Class1", "Class2", "Class3"]; // Replace with your actual class names
            const predictedClass = classLabels[topPredictionIndex];

            // Update the category select dropdown
            const categorySelect = document.getElementById('categorySelect');
            if (categorySelect) {
                categorySelect.value = predictedClass;
                alert(`Predicted category: ${predictedClass}`);
            }
        };
    };
    reader.readAsDataURL(file);
}
 

