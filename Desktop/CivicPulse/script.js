// Handle file selection for AI detection
async function handleFileSelect(e) {
    const files = e.target.files;
    
    if (files.length === 0) return;
    
    // Clear previous previews
    previewContainer.innerHTML = '';
    
    // Process each file
    for (let i = 0; i < files.length; i++) {
        const file = files[i];
        
        // Create preview
        const reader = new FileReader();
        reader.onload = function(event) {
            const previewItem = document.createElement('div');
            previewItem.className = 'preview-item';
            
            if (file.type.startsWith('image/')) {
                const img = document.createElement('img');
                img.src = event.target.result;
                previewItem.appendChild(img);
            } else if (file.type.startsWith('video/')) {
                const video = document.createElement('video');
                video.src = event.target.result;
                video.controls = true;
                previewItem.appendChild(video);
            }
            
            const removeBtn = document.createElement('button');
            removeBtn.className = 'preview-remove';
            removeBtn.innerHTML = '<i class="fas fa-times"></i>';
            removeBtn.addEventListener('click', function() {
                previewItem.remove();
            });
            
            previewItem.appendChild(removeBtn);
            previewContainer.appendChild(previewItem);
        };
        
        reader.readAsDataURL(file);
        
        // If it's an image, run AI detection
        if (file.type.startsWith('image/') && i === 0) { // Only process the first image
            // Show AI detection section
            aiDetection.classList.remove('hidden');
            aiDescription.textContent = 'Analyzing image...';
            
            try {
                const result = await detectProblemFromImage(file);
                
                // Update form with AI results
                categorySelect.value = result.category;
                descriptionInput.value = result.description;
                
                // Update AI detection UI
                aiDescription.innerHTML = `
                    <strong>AI Detected:</strong> ${result.originalLabel} 
                    <span style="color: var(--gray-color);">(${(result.confidence * 100).toFixed(1)}% confidence)</span><br>
                    <strong>Mapped to:</strong> ${result.category}
                `;
            } catch (error) {
                console.error('Error in AI detection:', error);
                aiDescription.textContent = 'Could not analyze image. Please describe the issue manually.';
            }
        }
    }
}

